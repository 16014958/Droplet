function geoFindMe() {
  var output = document.getElementById("r");
  var put = document.getElementById("b");
  var bay = document.getElementById("bay");
 var row = document.getElementById("row");
 var location = document.getElementById("currentLocation");
 var anyRow = document.getElementById("inputRow");


  if (row.style.display === "none") {
        row.style.display = "block";
    } else {
        row.style.display = "none";
    }


    if (output.style.display === "block" ){
      put.style.display = "none";
      output.style.display = "none";
      bay.style.display = "block";
    } else {
      output.style.display = "block" ;
      put.style.display = "block";
      bay.style.display = "none"
    }



  if (location.style.display === "none" ){
    location.style.display = "block";

  } else {
    location.style.display = "none" ;
  }


    if (anyRow.style.display === "block" ){
      anyRow.style.display = "none";

    } else {
      // output.style.display = "none";
      anyRow.style.display = "block" ;
    }







  if (!navigator.geolocation){
    output.innerHTML = "<p>Geolocation is not supported by your browser</p>";
    return;
  }

  function successO(position) {
    var latitudeO  = Math.round(position.coords.latitude);
    var longitudeO = Math.round(position.coords.longitude);





    output.innerHTML = '<input type="text" value=" ' + latitudeO + '   " name="row" required /> ';
    put.innerHTML = '<input type="text" value=" ' + longitudeO + '  "  name="bay" required />' ;


    // output.appendChild(img);
  }

  function errorO() {
    output.innerHTML = "Unable to retrieve your location";
  }

  output.innerHTML = "<p>Locatingâ€¦</p>";

  navigator.geolocation.getCurrentPosition(successO, errorO);
}









